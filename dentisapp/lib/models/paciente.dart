class Paciente {
  final int idPaciente;
  final String nombre;
  final String apellido;
  final String cedula;
  final String telefono;
  final String correo;

Paciente({
    required this.idPaciente,
    required this.nombre,
    required this.apellido,
    required this.cedula,
    required this.telefono,
    required this.correo,
  });
}
